package M07_MemArLis;

public class Ex03_Classes {
	private String name;
	private int idade;
	private double altura;
	
	public Ex03_Classes(String name, int idade, double altura) {
		super();
		this.name = name;
		this.idade = idade;
		this.altura = altura;
	}

	public String getName() {
		return name;
	}


	public int getIdade() {
		return idade;
	}


	public double getAltura() {
		return altura;
	}

}